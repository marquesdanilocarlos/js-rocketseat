let number = 10;

//Incremento
//Pós incremento
console.log(number++);

//Pré incremento
console.log(++number);

//Pós decremento
console.log(number--);

//Pré decremento
console.log(--number);

//Incremento com valores maiores que 1
number += 10;
console.log(number);

//Decremento com valores maiores que 1
number -= 15;
console.log(number);