let one = 1;
let two = 2;

//Igualdade
console.log(one == two);
console.log(one == 1);
console.log(one == "1");
console.log(one === "1");

//Diferença
console.log(one != two);
console.log(one != 1);
console.log(one != "1");
console.log(one !== "1");

let balance = 500;
let payment = 120;

//Maior ou igual
console.log(balance > payment);
console.log(balance >= payment);

//Menor ou igual
console.log(balance < payment);
console.log(balance <= payment);

//Condicionais

let email = true;
let password = false;

//AND (E) &&
console.log(email && password);

//OR (OU) ||
console.log(email || password);

//NOT (negação)
console.log(!password);